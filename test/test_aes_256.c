    byte block[16] = { 0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff };
    byte out[16];
    byte res[16];
    word * w = malloc(AES->block_wsize * (AES->rounds + 1) * sizeof(word));

    key_expansion(key, w);

    cipher(block, out, w);
